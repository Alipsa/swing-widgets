package se.alipsa.datepicker;

/**
 * Defines where the text field is placed relative to the calendar button in a {@link DatePicker}.
 */
public enum TextFieldPosition {
  /** Places the text field to the left of the calendar button. */
  LEFT,
  /** Places the text field to the right of the calendar button. */
  RIGHT,
  /** Places the text field above the calendar button. */
  ABOVE,
  /** Places the text field below the calendar button. */
  BELOW
}
